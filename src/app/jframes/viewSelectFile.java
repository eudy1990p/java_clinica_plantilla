    private void jFileChooser1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFileChooser1ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = (JFileChooser) evt.getSource();
        
        String command = evt.getActionCommand();
        
        if(command.equals(JFileChooser.APPROVE_SELECTION)){
            
            File file = fileChooser.getSelectedFile();
            String path = file.getAbsolutePath();
            String name = file.getName();
            boolean respuesta = this.copyFile(path, name);
            if(respuesta){
                this.padre.setImage(this.nameFile);
                this.dispose();
                JOptionPane.showMessageDialog(null, "archivo copiado ? "+this.nameFile+" ? "+path);
                
            }else{
                JOptionPane.showMessageDialog(null, "no se pudo copiar");
            }
            
            
        }else if(command.equals(JFileChooser.CANCEL_SELECTION)){
            this.dispose();
        }
        
    }//GEN-LAST:event_jFileChooser1ActionPerformed

    private boolean copyFile(String path,String name){
          try{
              this.nameFile ="C:/img_p/"+name;
            FileInputStream fis = new FileInputStream(path); //inFile -> Archivo a copiar
            FileOutputStream fos = new FileOutputStream(this.nameFile); //outFile -> Copia del archivo
            FileChannel inChannel = fis.getChannel(); 
            FileChannel outChannel = fos.getChannel(); 
            inChannel.transferTo(0, inChannel.size(), outChannel); 
            fis.close(); 
            fos.close();
            return true;
        }catch (IOException ioe) {
            System.err.println("Error al Generar Copia");
            return false;
        }
    }
    
